    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if (jtMunicipios.getSelectedRow() >= 0) {//osea si tengo un renglon seleccionado procedo si no pues no
            int idMunicipio = (int) jtMunicipios.getValueAt(jtMunicipios.getSelectedRow(), 0);//getValueAt es tomar un valor en tal coordenada ¿Cual coordenada? R=el renglon es x y y osea el renglon seleccionado y en la columna cero, osea la primerita que es el idProducto y lo alaceno en la variable de tipo int que se llama idProducto que esta declarada en esta misma linea de codigo
            frmNMMunicipios obj = new frmNMMunicipios(idMunicipio);//le mandamos idProducto
            obj.setTitle("Modificar Municipio");
            obj.setModal(true);
            obj.setVisible(true);
            jtMunicipios.setModel(new Municipios().GetAllModel());
        } else {
            JOptionPane.showMessageDialog(rootPane, "Debes seleccionar un registro");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        Municipios municipios = new Municipios();
         if (jtMunicipios.getSelectedRow() >= 0) {//osea si tengo un renglon seleccionado procedo si no pues no
            int idMunicipio = (int) jtMunicipios.getValueAt(jtMunicipios.getSelectedRow(), 0);//getValueAt es tomar un valor en tal coordenada ¿Cual coordenada? R=el renglon es x y y osea el renglon seleccionado y en la columna cero, osea la primerita que es el idProducto y lo alaceno en la variable de tipo int que se llama idProducto que esta declarada en esta misma linea de codigo
            jtMunicipios.setModel(new Municipios().GetAllModel());
            municipios.setIdMunicipio(idMunicipio);
            if (municipios.Delete()) {
                JOptionPane.showMessageDialog(null, "Municipio eliminado correctamente");
                jtMunicipios.setModel(new Municipios().GetAllModel());
                
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Debes de seleccionar un registro");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

