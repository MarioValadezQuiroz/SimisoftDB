    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
         if (jtLocalidades.getSelectedRow() >= 0) {//osea si tengo un renglon seleccionado procedo si no pues no
            int idLocalidades = (int) jtLocalidades.getValueAt(jtLocalidades.getSelectedRow(), 0);//getValueAt es tomar un valor en tal coordenada ¿Cual coordenada? R=el renglon es x y y osea el renglon seleccionado y en la columna cero, osea la primerita que es el idProducto y lo alaceno en la variable de tipo int que se llama idProducto que esta declarada en esta misma linea de codigo
            frmNMLocalidades obj = new frmNMLocalidades(idLocalidades);//le mandamos idProducto
            obj.setTitle("Modificar Localidad");
            obj.setModal(true);
            obj.setVisible(true);
            jtLocalidades.setModel(new Localidades().GetAllModel());
        } else {
            JOptionPane.showMessageDialog(rootPane, "Debes seleccionar un registro");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

