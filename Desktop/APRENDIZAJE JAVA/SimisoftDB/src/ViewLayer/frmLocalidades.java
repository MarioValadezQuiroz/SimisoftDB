    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        Localidades localidad = new Localidades();
         if (jtLocalidades.getSelectedRow() >= 0) {//osea si tengo un renglon seleccionado procedo si no pues no
            int idLocalidad = (int) jtLocalidades.getValueAt(jtLocalidades.getSelectedRow(), 0);//getValueAt es tomar un valor en tal coordenada ¿Cual coordenada? R=el renglon es x y y osea el renglon seleccionado y en la columna cero, osea la primerita que es el idProducto y lo alaceno en la variable de tipo int que se llama idProducto que esta declarada en esta misma linea de codigo
            jtLocalidades.setModel(new Localidades().GetAllModel());
            localidad.setIdLocalidad(idLocalidad);
            if (localidad.Delete()) {
                JOptionPane.showMessageDialog(null, "Localidad eliminada correctamente");
                jtLocalidades.setModel(new Localidades().GetAllModel());
                
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Debes de seleccionar un registro");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed


